package com.order.model;

public class JsonResponse {

	private String response;
	
	public JsonResponse() {
		super();
	}
	
	public JsonResponse(String response) {
		super();
		this.response = response;
	}

	public String getResponse() {
		return response;
	}

	public void setResponse(String response) {
		this.response = response;
	}
	
}
